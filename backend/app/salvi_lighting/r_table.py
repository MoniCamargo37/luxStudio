def r_value(tan_gamma, beta_deg, road='R3'):
    """Return reduced luminance coefficient r (not r*10000) using bilinear interpolation.
    Returns 0 for geometrically invalid angle combinations (empty table cells)."""
    TABLE, _ = _TABLES[road]
    tg = max(0.0, min(12.0, tan_gamma))
    bd = abs(beta_deg) % 360
    if bd > 180:
        bd = 360 - bd

    i = bisect.bisect_right(TANG, tg) - 1
    i = max(0, min(i, len(TANG) - 2))
    j = bisect.bisect_right(BETAS, bd) - 1
    j = max(0, min(j, len(BETAS) - 2))

    v00 = TABLE[i][j]
    v01 = TABLE[i][j + 1]
    v10 = TABLE[i + 1][j]
    v11 = TABLE[i + 1][j + 1]

    # If any corner has no data (0), geometry is outside valid range
    if v00 == 0 or v01 == 0 or v10 == 0 or v11 == 0:
        return 0.0

    t = (
        (tg - TANG[i]) / (TANG[i + 1] - TANG[i])
        if TANG[i + 1] != TANG[i]
        else 0.0
    )
    u = (
        (bd - BETAS[j]) / (BETAS[j + 1] - BETAS[j])
        if BETAS[j + 1] != BETAS[j]
        else 0.0
    )
    v = (
        (1 - t) * (1 - u) * v00
        + (1 - t) * u * v01
        + t * (1 - u) * v10
        + t * u * v11
    )
    return v / 10000.0
